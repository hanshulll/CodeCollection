class Solution{
    public:
    // arr: input array
    // n: size of array
    //Function to find the sum of contiguous subarray with maximum sum.
    long long maxSubarraySum(int arr[], int n){
        
        // Your code here
        long long ms = arr[0], cs = arr[0];
        for(int i=1;i<n;i++){
            if(cs < 0){
                cs = 0;
            }
            cs += arr[i];
            ms = max(ms, cs);
        }
        return ms;
    }
};

